import axios from 'axios';

import { IRepresentacionGrafica } from '@/shared/model/representacion-grafica.model';

const baseApiUrl = 'api/representacion-graficas';

export default class RepresentacionGraficaService {
  public find(id: number): Promise<IRepresentacionGrafica> {
    return new Promise<IRepresentacionGrafica>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IRepresentacionGrafica): Promise<IRepresentacionGrafica> {
    return new Promise<IRepresentacionGrafica>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IRepresentacionGrafica): Promise<IRepresentacionGrafica> {
    return new Promise<IRepresentacionGrafica>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IRepresentacionGrafica): Promise<IRepresentacionGrafica> {
    return new Promise<IRepresentacionGrafica>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
